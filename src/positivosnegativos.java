public class positivosnegativos {
    public static void main(String[] args) {
        int numeroIf = -6;

        if (numeroIf > 0){
            System.out.println(numeroIf + " " + "Es número positivo");
        }
        else{
            System.out.println(numeroIf + " " + "Es número negativo" );
        }

    }
}
